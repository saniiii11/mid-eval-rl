import numpy as np

class Policy:
    def __init__(self, n_arms, horizon):
        self.n_arms = n_arms
        self.horizon = horizon

        # Track statistics for each ad
        self.counts = np.zeros(n_arms)
        self.values = np.zeros(n_arms)
        self.t = 0

    def select_arm(self):
        self.t += 1

        # 1. Initialization: Pull each ad once to prevent division by zero
        if self.t <= self.n_arms:
            return self.t - 1

        # 2. Dynamic Exploration Decay
        # Measures progress from 0.0 (start) to 1.0 (end of budget)
        progress = self.t / self.horizon

        # The exploration multiplier 'c' shrinks linearly as time runs out.
        # At step 1, c ≈ 0.55. At step 5000, c becomes exactly 0.0.
        c = 0.55 * (1.0 - progress)

        # 3. Compute UCB values with the dynamic bonus
        exploration_bonus = c * np.sqrt(np.log(self.t) / self.counts)
        ucb_values = self.values + exploration_bonus

        # Break ties randomly to ensure unbiased exploration early on
        max_ucb = np.max(ucb_values)
        best_arms = np.where(ucb_values == max_ucb)[0]
        return int(np.random.choice(best_arms))

    def update(self, arm, reward):
        self.counts[arm] += 1
        # Incremental average update for stability
        self.values[arm] += (reward - self.values[arm]) / self.counts[arm]